const unresolvedModelPattern =
  /^(옵션 상담|상담 확인|확인 중|모델 확인|약정 옵션 확인)$/i;

export function hasSpecificRentalModel(product: RentalProduct) {
  const model = product.modelName.trim();
  return Boolean(model) && !unresolvedModelPattern.test(model);
}

export function hasRentalProductImage(product: RentalProduct) {
  if (!product.image) return false;
  return !product.image.startsWith("/rental/placeholders/");
}

export function isRentalProductReady(product: RentalProduct) {
  return hasSpecificRentalModel(product) && hasRentalProductImage(product);
}

export function getBrands(category: RentalCategory) {
  const list =
    category === "전체"
      ? rentalProducts
      : rentalProducts.filter((product) => product.category === category);

  return Array.from(new Set(list.map((product) => product.brand))).sort(
    (a, b) => a.localeCompare(b, "ko"),
  );
}

export function getCategoryCount(category: RentalCategory) {
  if (category === "전체") return rentalProducts.length;
  return rentalProducts.filter((product) => product.category === category)
    .length;
}
